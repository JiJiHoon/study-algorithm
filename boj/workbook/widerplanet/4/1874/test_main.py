from unittest import TestCase

from main import solve


class Test(TestCase):
    def test_solve(self):
        test_cases = [
            [[1, 2, 5, 3, 4], ['NO']],
            [[5, 4, 1, 2, 3], ['NO']],
            [[5, 1, 2, 3, 4], ['NO']],
            [[5, 2, 1, 4, 3], ['NO']],
            [[1, 4, 2, 3], ['NO']],
            [[3, 1, 2], ['NO']],
            [[1, 3, 2], ['+', '-', '+', '+', '-', '-']],
            [[1], ['+', '-']],
            [[1, 2], ['+', '-', '+', '-']],
            [[2, 1], ['+', '+', '-', '-']],
            [[5, 4, 3, 2, 1], ['+', '+', '+', '+', '+', '-', '-', '-', '-', '-']],
            [[1, 2, 3, 4, 5], ['+', '-', '+', '-', '+', '-', '+', '-', '+', '-']],
            [[1, 3, 2, 4], ['+', '-', '+', '+', '-', '-', '+', '-']],
            [[1, 3, 4, 2, 6, 5], ['+', '-', '+', '+', '-', '+', '-', '-', '+', '+', '-', '-']],
            [[4, 3, 6, 8, 7, 5, 2, 1],
             ['+', '+', '+', '+', '-', '-', '+', '+', '-', '+', '+', '-', '-', '-', '-', '-', ]],
        ]

        for test_case in test_cases:
            data, expected = test_case

            actual = solve(data)

        self.assertEqual(expected, actual)
